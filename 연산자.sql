-- AGE 칼럼의 값이 NULL인 ROW만 출력
SELECT NAME, AGE
FROM TEMP_TB
--WHERE AGE IS NULL
WHERE AGE IS NOT NULL
;

-- AGE가 19 초과 이면서 21 이하는 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
WHERE AGE > 10
AND AGE <= 21
;

-- AGE가 20 이하 이거나, 22 이상인 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
WHERE AGE <= 20
OR AGE >= 22
;

-- AGE가 21~22 사이에 해당하는 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
WHERE AGE BETWEEN 25 AND 26
;

-- NAME 이 '4G' 또는 '3A' 또는 '2B' 또는 '9S' 인 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
--WHERE NAME IN('4G', '3A', '2B', '9S')
WHERE NAME NOT IN('2B', '9S')
;

-- 이름이 4로 시작하는 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
WHERE NAME LIKE '%A%'
--WHERE NAME NOT LIKE '%A%'
;

--이름이 '김'으로 시작하는(3글자 이름을 가진) ROW 출력
