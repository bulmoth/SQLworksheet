SELECT NAME, AGE, '9S' AS "내가 만든 칼럼" FROM TEMP_TB;
SELECT NAME, AGE, '9S' 내가만든칼럼 FROM TEMP_TB;
SELECT NAME, AGE, AGE 내가만든칼럼 FROM TEMP_TB;
SELECT NAME, AGE, AGE+3 마이칼럼 FROM TEMP_TB;

-- 문자열 더하기 ||
SELECT NAME, AGE FROM TEMP_TB;
SELECT NAME, AGE, NAME 내가만든칼럼 FROM TEMP_TB;
SELECT NAME, AGE, NAME+1 내가만든칼럼 FROM TEMP_TB; -- 실패
SELECT NAME, AGE, 1+1 내가만든칼럼 FROM TEMP_TB;
SELECT NAME, AGE, 1+'1' 내가만든칼럼 FROM TEMP_TB; -- 숫자+문자 => 숫자
SELECT NAME, AGE, 1||'1' 내가만든칼럼 FROM TEMP_TB; -- 숫자||문자 => 문자
SELECT NAME, AGE, 7 내가만든칼럼 FROM TEMP_TB; -- 숫자는 오른쪽 정렬
SELECT NAME, AGE, '7' 내가만든칼럼 FROM TEMP_TB; -- 문자는 왼쪽 정렬
SELECT NAME, AGE, NAME+'9S' 내가만든칼럼 FROM TEMP_TB; -- 실패
SELECT NAME, AGE, NAME||' YORHA' 내가만든칼럼 FROM TEMP_TB; -- 문자열 더하기 연산자
SELECT NAME, AGE, CONCAT(NAME,' YORHA') 내가만든칼럼 FROM TEMP_TB; -- CONCAT() 함수 사용

SELECT NAME||'('||AGE||')' "이름(나이)" FROM TEMP_TB; 

SELECT SYSDATE FROM TEMP_TB;

SELECT 123+1 FROM DUAL;

-- 연산자
/*
    = : 같다
    !=, ^=, <> : 같지 않다
    >, <, >=, <= : 대소 비교
    IS NULL : NULL 인지 확인
    IS NOT NULL : NULL이 아닌지 확인
    
    NOT : 부정연산자
    AND : 그리고(== 자바의 &&)
    OR : 또는(== 자바의 ||)
    BETWEEN A AND B : A와 B 사이 인지 확인
    IN, NOT IN : 포함 되는지 확인
    
    LIKE, NOT LIKE : 문자열 비교
    % : 0개 이상의 문자열
    _ : 한글자
*/

SELECT * FROM TEMP_TB;

SELECT NAME, AGE FROM TEMP_TB; -- 모든 행 출력

-- 나이가 20인 ROW만 출력
SELECT NAME, AGE 
FROM TEMP_TB 
WHERE AGE=20; 

-- 나이가 20이 아닌 ROW만 출력
SELECT NAME, AGE 
FROM TEMP_TB 
WHERE AGE!=20
--WHERE AGE^=20
--WHERE AGE<>20
;

-- 나이가 20 초과인 ROW 출력
SELECT NAME, AGE
FROM TEMP_TB
WHERE AGE>20
;

