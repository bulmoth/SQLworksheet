-- 연산자 연습용 테이블 생성 (삭제 예정)
CREATE TABLE TEMP_TB(
      NAME VARCHAR2(100)
    , AGE NUMBER
);

INSERT INTO TEMP_TB(NAME, AGE) VALUES('4G', 26);
