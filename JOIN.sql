SELECT *
FROM EMP
;

SELECT *
FROM DEPT
;

SELECT 
      ENAME 사원명
    , E.DEPTNO 부서번호
    , DNAME 부서이름
FROM EMP E
INNER JOIN DEPT D ON E.DEPTNO = D.DEPTNO --ANSI표준
;

SELECT 
      ENAME 사원명
    , E.DEPTNO 부서번호
    , DNAME 부서이름
FROM EMP E, DEPT D
WHERE E.DEPTNO = D.DEPTNO --ORACLE지원
;